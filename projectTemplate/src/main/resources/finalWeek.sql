CREATE TABLE CAR_PARTS(
   PART_NUMBER		VARCHAR(64) NOT NULL PRIMARY KEY,
   TITLE 			VARCHAR(50) NOT NULL,
   LINE				VARCHAR(50) NOT NULL,
   DESCRIPTION 		VARCHAR(256) NOT NULL,
   PRICE			DECIMAL NOT NULL,
   QTY_ON_HAND		INTEGER NOT NULL
);

CREATE TABLE CARS (
	VID 		VARCHAR(64) PRIMARY KEY,
	C_YEAR 		INTEGER,
	MAKE 		VARCHAR(64),
	MODEL 		VARCHAR(64)
);
 
CREATE TABLE COMPATIBILITY (
	C_ID 			INTEGER 	PRIMARY KEY NOT NULL GENERATED ALWAYS AS IDENTITY,
	PART_NUMBER		VARCHAR(64),
	VID 			VARCHAR	(64),
	CONSTRAINT 	fk_PART_NUMBER FOREIGN KEY (PART_NUMBER) REFERENCES CAR_PARTS(PART_NUMBER),
	CONSTRAINT 	fk_VID FOREIGN KEY (VID) REFERENCES CARS(VID)
);

CREATE TABLE SALES (
	S_ID 			INTEGER PRIMARY KEY NOT NULL GENERATED ALWAYS AS IDENTITY,
	PURCHASE_DATE 	TIMESTAMP,
	PART_NUMBER 	VARCHAR(64),
	FIRST_NAME 		VARCHAR(64),
	LAST_NAME 		VARCHAR(64),
	ADDRESS 		VARCHAR(256),
	CITY 			VARCHAR(64),
	STATE	 		VARCHAR(64),
	ZIP 			INTEGER ,
	TENDERTYPE 		VARCHAR(64),
	TENDERAMOUNT 	DECIMAL,
	CHANGE 			DECIMAL
);

